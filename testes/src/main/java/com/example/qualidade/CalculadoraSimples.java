package com.example.qualidade;

public class CalculadoraSimples {

    public double somar(double valor_1, double valor_2) {
        return valor_1 + valor_2;
    }

    public double dividir(double valor_1, double valor_2) {
        return valor_1 / valor_2;
    }

    public double subtrair(double valor_1, double valor_2) {
        return valor_1 - valor_2;
    }

    public double multiplicar(double valor_1, double valor_2) {
        return valor_1 * valor_2;
    }
}